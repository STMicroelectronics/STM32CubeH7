static void GoTo_SubDemo(const uint32_t thirdPartyAdress)
{
  /* Set the jump address */
  Set_DemoAddress(thirdPartyAdress);

  LCD_DeInitSequence();
  hltdc.Instance = LTDC;
  HAL_LTDC_DeInit(&hltdc);
  HAL_DMA2D_DeInit(&hdma2d);

  // HAL_Delay(200);

  /* Disable the MPU */
  HAL_MPU_Disable();
  
  /* Disable and Invalidate I-Cache */
  SCB_DisableICache();
  SCB_InvalidateICache();
  
  /* Disable, Clean and Invalidate D-Cache */
  SCB_DisableDCache();
  SCB_CleanInvalidateDCache();
  
  /* Disable the TIM6 global Interrupt */
  HAL_NVIC_DisableIRQ(TIM6_DAC_IRQn);
  HAL_TIM_Base_DeInit(&htim6);

  /* Reset the system */
  NVIC_SystemReset();
}

