void Error_Handler(void)
{
  while (1)
  {
    BSP_LED_Toggle(LED_RED);
    osDelay(100);
  }
}

void Success_Handler(void)
{
  while (1)
  {
    BSP_LED_Toggle(LED_GREEN);
    osDelay(200);
  }
}
